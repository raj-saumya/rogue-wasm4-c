void Game_init(GameContext *gc) {
  gc->frame_count = 0;
  gc->screen_state = GAME_START_SCREEN;
  gc->player.health = 100;
  Player_init(gc);
  for (int i = 0; i < DEMON_COUNT; i++) {
    gc->demons[i].is_alive = false;
    gc->demons[i].pos_x = 0;
    gc->demons[i].pos_y = 0;
  }
}

void Game_startScreen(GameContext *gc, uint8_t gamepad) {
  if (gamepad & BUTTON_2) {
    gc->screen_state = GAME_PROGRESS_SCREEN;
    return;
  }

  *DRAW_COLORS = 0x21;
  blit(START_SCREEN_SPRITE, 0, 0, 320, 200, 0);

  *DRAW_COLORS = 3;
  text("Welcome to", 40, 10);
  text("The Rogue", 40, 30);
  text("Of", 68, 42);
  text("Abyss", 58, 52);
  text("Press \x81 to start", 16, 130);

  Player_render(gc);
}

void Game_spawnDemons(GameContext *gc) {
  gc->frame_count++;

  if (gc->frame_count % DEMON_SPAWN_INTERVAL == 0) {
    for (int i = 0; i < DEMON_COUNT; i++) {
      if (!gc->demons[i].is_alive) {
        gc->demons[i].is_alive = true;
        if (rand() % 2 == 0) {
          gc->demons[i].pos_x = rand() % SCREEN_SIZE;
          gc->demons[i].pos_y = (rand() % 2 == 0) ? 0 : SCREEN_SIZE;
        } else {
          gc->demons[i].pos_x = (rand() % 2 == 0) ? 0 : SCREEN_SIZE;
          gc->demons[i].pos_y = rand() % SCREEN_SIZE;
        }
        break;
      }
    }
  }

  Demon_swarm_tracking(gc);
  Demon_render_all(gc);
}

void Game_PlayerHealthStatus(GameContext *gc) {
  if (gc->player.health <= 0) {
    gc->screen_state = GAME_OVER_SCREEN;
    return;
  }

  // health bar container
  *DRAW_COLORS = 0x31;
  int32_t size = (int32_t)(SCREEN_SIZE - 8);
  rect(4, size, (uint32_t)size, 3);

  uint32_t health_width = (uint32_t)((gc->player.health * size - 2) / 100);

  *DRAW_COLORS = 3;
  rect(5, size, health_width, 2);
}

void Game_progressScreen(GameContext *gc, uint8_t gamepad) {

  if (gamepad & BUTTON_LEFT) {
    gc->player.dir = BLIT_2BPP | BLIT_FLIP_X;
    Player_move(gc, -1, 0);
  } else if (gamepad & BUTTON_RIGHT) {
    gc->player.dir = BLIT_2BPP;
    Player_move(gc, 1, 0);
  } else if (gamepad & BUTTON_UP) {
    Player_move(gc, 0, -1);
  } else if (gamepad & BUTTON_DOWN) {
    Player_move(gc, 0, 1);
  } else {
    Player_render(gc);
  }

  Game_spawnDemons(gc);
  Game_PlayerHealthStatus(gc);
}

void Game_overScreen(GameContext *gc, uint8_t gamepad) {
  if (gamepad & BUTTON_2) {
    Game_init(gc);
    gc->screen_state = GAME_PROGRESS_SCREEN;
    return;
  }

  *DRAW_COLORS = 0x21;
  blit(START_SCREEN_SPRITE, 0, 0, 320, 200, 0);

  *DRAW_COLORS = 3;
  text("Game Over", 40, 10);
  // scorecard
  text("Level: 4", 48, 40);
  text("Time Alive: 0:00", 18, 55);
  text("Demons Slayed: 33", 12, 70);
  text("Buffs: 8", 48, 85);

  text("Press \x81 to restart", 8, 130);
}