void Game_init(GameContext *gc) {
  gc->frame_count = 0;
  gc->score = 0;
  gc->screen_state = GAME_START_SCREEN;
  gc->player.health = 100;
  Player_init(gc);
  for (int i = 0; i < DEMON_COUNT; i++) {
    gc->demons[i].is_alive = false;
    gc->demons[i].pos_x = 0;
    gc->demons[i].pos_y = 0;
  }
}

void Game_startScreen(GameContext *gc, uint8_t gamepad) {
  if (gamepad & BUTTON_2) {
    gc->screen_state = GAME_PROGRESS_SCREEN;
    return;
  }

  *DRAW_COLORS = 0x21;
  blit(START_SCREEN_SPRITE, 0, 0, 320, 200, 0);

  *DRAW_COLORS = 3;
  text("The Rogue", 40, 20);
  text("Of", 68, 32);
  text("Abyss", 58, 42);
  text("Press \x81 to start", 16, 130);

  Player_render(gc);
}

void Game_spawnDemons(GameContext *gc) {
  gc->frame_count++;

  if (gc->frame_count % DEMON_SPAWN_INTERVAL == 0) {
    for (int i = 0; i < DEMON_COUNT; i++) {
      if (!gc->demons[i].is_alive) {
        gc->demons[i].is_alive = true;
        gc->demons[i].health = 2;
        if (rand() % 2 == 0) {
          gc->demons[i].pos_x = rand() % SCREEN_SIZE;
          gc->demons[i].pos_y = (rand() % 2 == 0) ? 0 : SCREEN_SIZE;
        } else {
          gc->demons[i].pos_x = (rand() % 2 == 0) ? 0 : SCREEN_SIZE;
          gc->demons[i].pos_y = rand() % SCREEN_SIZE;
        }
        break;
      }
    }
  }

  Demon_swarm_tracking(gc);
  Demon_render_all(gc);
}

void Game_PlayerHealthStatus(GameContext *gc) {
  if (gc->player.health <= 0) {
    gc->screen_state = GAME_OVER_SCREEN;
    return;
  }

  // health bar container
  int32_t size = (int32_t)(DEMON_SPRITE_FRAME_SIZE);
  int32_t x = gc->player.pos_x;
  int32_t y = gc->player.pos_y + DEMON_SPRITE_H + 2;

  uint32_t health_width = (uint32_t)((gc->player.health * size) / 100);

  *DRAW_COLORS = 3;
  rect(x, y, health_width, 1);
}

void Game_progressScreen(GameContext *gc, uint8_t gamepad) {

  if (gamepad & BUTTON_LEFT) {
    gc->player.dir = BLIT_2BPP | BLIT_FLIP_X;
    Player_move(gc, -1, 0);
  } else if (gamepad & BUTTON_RIGHT) {
    gc->player.dir = BLIT_2BPP;
    Player_move(gc, 1, 0);
  } else if (gamepad & BUTTON_UP) {
    Player_move(gc, 0, -1);
  } else if (gamepad & BUTTON_DOWN) {
    Player_move(gc, 0, 1);
  } else {
    Player_render(gc);
  }

  Game_spawnDemons(gc);
  Game_PlayerHealthStatus(gc);
  Player_update_projectiles(gc);
  Player_attacks(gc);
}

void Game_overScreen(GameContext *gc, uint8_t gamepad) {
  if (gamepad & BUTTON_2) {
    Game_init(gc);
    gc->screen_state = GAME_PROGRESS_SCREEN;
    return;
  }

  *DRAW_COLORS = 0x21;
  blit(START_SCREEN_SPRITE, 0, 0, 320, 200, 0);

  *DRAW_COLORS = 3;
  text("Game Over", 40, 10);
  // scorecard
  text("Level: 4", 48, 40);
  text("Time Alive: 0:00", 18, 55);

  // Build "Demons Slayed: X" string
  char slayed_str[32] = "Demons Slayed: ";
  int s = (int)gc->score;
  char num[12];
  int idx = 0;
  if (s == 0) {
    num[idx++] = '0';
  } else {
    while (s > 0 && idx < 10) {
      num[idx++] = (char)((s % 10) + '0');
      s /= 10;
    }
  }
  // Reverse num
  for (int j = 0; j < idx / 2; j++) {
    char tmp = num[j];
    num[j] = num[idx - 1 - j];
    num[idx - 1 - j] = tmp;
  }
  num[idx] = '\0';
  // Append to slayed_str
  int len = 15; // length of "Demons Slayed: "
  for (int j = 0; j <= idx; j++) {
    slayed_str[len + j] = num[j];
  }
  text(slayed_str, 12, 70);

  text("Buffs: 8", 48, 85);

  text("Press \x81 to restart", 8, 130);
}